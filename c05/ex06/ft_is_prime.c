int	ft_is_prime(int nb)
{
	int i;
	int test;

	i = 2;
	test = 1;
	if (nb < 0)
	{
		return (0);
	}
	if (nb <= 1)
		return (0);
	while ((i * i) <= nb)
	{
		if ((nb % i) == 0)
			test++;
		i++;
	}
	if (test == 1)
		return (1);
	else
		return (0);
}
