#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int *tab;
	int i;

	if ((max - min) < 0)
		return (NULL);
	tab = malloc(sizeof(int) * max - min);
	if (tab == NULL)
		return (NULL);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		min++;
		i++;
	}
	return (tab);
}
