void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int a;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		a = tab[i];
		tab[i] = tab[j];
		tab[j] = a;
		i++;
		j--;
	}
}
